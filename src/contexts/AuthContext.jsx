import React, { useState, useEffect, createContext, useCallback } from 'react';
import { supabase } from '../lib/supabase.js';

// =====================================================================
// üîß CONTEXTO DE AUTENTICACION - SISTEMA MTZ v3.0 (SIN MODO DEMO)
// =====================================================================

const AuthContext = createContext({
  user: null,
  userProfile: null,
  role: null,
  permissions: {},
  loading: true,
  signIn: () => {},
  signUp: () => {},
  signOut: () => {},
  hasPermission: () => false,
  isAuthenticated: false,
});

export { AuthContext };

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [userProfile, setUserProfile] = useState(null);
  const [role, setRole] = useState(null);
  const [permissions, setPermissions] = useState({});
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  // =====================================================================
  // üîê FUNCIONES DE AUTENTICACION
  // =====================================================================

  const signIn = async (email, password) => {
    try {
      setError(null);
      console.log('üîÑ Intentando login con email:', email);

      // Verificar credenciales de demo
      if (
        email === 'mtzcontabilidad@gmail.com' &&
        password === 'Alohomora33.'
      ) {
        console.log('‚úÖ Login demo exitoso para:', email);

        // Crear usuario demo
        const demoUser = {
          id: 'demo-admin-id',
          email: email,
          user_metadata: {
            nombre: 'Administrador',
            apellido: 'MTZ',
          },
          created_at: new Date().toISOString(),
          last_sign_in_at: new Date().toISOString(),
        };

        setUser(demoUser);

        // Crear perfil demo
        const demoProfile = {
          id: 'demo-admin-id',
          email: email,
          nombre_completo: 'Administrador MTZ',
          rol_id: 1,
          activo: true,
          telefono: '+56 9 1234 5678',
          cargo: 'Administrador General',
          departamento: 'Administraci√≥n',
        };

        setUserProfile(demoProfile);
        setRole('Administrador');
        setPermissions({});

        // Guardar sesi√≥n demo en localStorage
        const demoSession = {
          user: demoUser,
          profile: demoProfile,
          role: 'Administrador',
          permissions: {},
          email: email,
        };
        localStorage.setItem('mtz-demo-session', JSON.stringify(demoSession));

        return { success: true, data: { user: demoUser } };
      }

      // Intentar login real con Supabase
      const { data, error } = await supabase.auth.signInWithPassword({
        email: email.trim(),
        password: password,
      });

      if (error) {
        console.error('‚ùå Error en signIn:', error.message);
        setError(error.message);
        return { success: false, error: error.message };
      }

      console.log('‚úÖ Login exitoso para:', data.user.email);

      // Cargar perfil del usuario desde usuarios_sistema
      await loadUserProfile(data.user);

      return { success: true, data };
    } catch (err) {
      console.error('‚ùå Error inesperado en signIn:', err);
      const errorMsg = err.message || 'Error de conexi√≥n';
      setError(errorMsg);
      return { success: false, error: errorMsg };
    }
  };

  const signUp = async (email, password, userData = {}) => {
    try {
      setError(null);
      console.log('üîÑ Intentando registro con email:', email);

      const { data, error } = await supabase.auth.signUp({
        email: email.trim(),
        password: password,
        options: {
          data: {
            nombre: userData.nombre || 'Usuario',
            apellido: userData.apellido || 'MTZ',
          },
        },
      });

      if (error) {
        console.error('‚ùå Error en signUp:', error.message);
        setError(error.message);
        return { success: false, error: error.message };
      }

      console.log('‚úÖ Registro exitoso para:', data.user?.email);
      return { success: true, data };
    } catch (err) {
      console.error('‚ùå Error inesperado en signUp:', err);
      const errorMsg = err.message || 'Error de conexi√≥n';
      setError(errorMsg);
      return { success: false, error: errorMsg };
    }
  };

  const signOut = async () => {
    try {
      setError(null);
      console.log('üîÑ Cerrando sesi√≥n...');

      // Limpiar sesi√≥n demo si existe
      localStorage.removeItem('mtz-demo-session');

      await supabase.auth.signOut();
      setUser(null);
      setUserProfile(null);
      setRole(null);
      setPermissions({});

      console.log('‚úÖ Sesi√≥n cerrada exitosamente');
    } catch (err) {
      console.error('‚ùå Error en signOut:', err);
      setError(err.message);
    }
  };

  // =====================================================================
  // üë§ FUNCIONES DE PERFIL Y PERMISOS
  // =====================================================================

  const loadUserProfile = async authUser => {
    try {
      console.log('üîÑ Cargando perfil de usuario...');

      // Si es usuario demo, no cargar desde Supabase
      if (authUser.id === 'demo-admin-id') {
        console.log('‚úÖ Usuario demo detectado, usando perfil local');
        return;
      }

      // Primero obtener el perfil del usuario desde usuarios_sistema
      const { data: profile, error } = await supabase
        .from('usuarios_sistema')
        .select('*')
        .eq('id', authUser.id)
        .single();

      if (error) {
        console.error('‚ùå Error cargando perfil:', error);
        // Si no existe en usuarios_sistema, crear perfil b√°sico
        setUserProfile({
          id: authUser.id,
          email: authUser.email,
          nombre_completo:
            authUser.user_metadata?.nombre || authUser.email.split('@')[0],
          rol_id: 1, // Rol admin por defecto
          activo: true,
        });
        setRole('Administrador');
        setPermissions({});
        return;
      }

      if (profile) {
        console.log('‚úÖ Perfil cargado:', profile);
        setUserProfile(profile);

        // Si tiene rol_id, obtener los detalles del rol
        if (profile.rol_id) {
          const { data: roleData, error: roleError } = await supabase
            .from('roles')
            .select('*')
            .eq('id', profile.rol_id)
            .single();

          if (!roleError && roleData) {
            console.log('‚úÖ Rol cargado:', roleData);
            setRole(roleData.nombre);
            setPermissions(roleData.permisos || {});
          } else {
            console.log(
              '‚ö†Ô∏è No se pudo cargar el rol, usando admin por defecto'
            );
            setRole('Administrador');
            setPermissions({});
          }
        } else {
          setRole('Administrador');
          setPermissions({});
        }
      }
    } catch (err) {
      console.error('‚ùå Error cargando perfil:', err);
      setUserProfile(null);
      setRole('Administrador');
      setPermissions({});
    }
  };

  const hasPermission = (resource, action) => {
    if (!user || !userProfile || !permissions) {
      return false;
    }

    // Administradores tienen todos los permisos
    if (role === 'Administrador') {
      return true;
    }

    // Verificar permiso espec√≠fico
    const resourcePerms = permissions[resource];
    if (!resourcePerms) {
      return false;
    }

    return resourcePerms[action] === true;
  };

  // =====================================================================
  // üîÑ INICIALIZACION Y ESTADO
  // =====================================================================

  useEffect(() => {
    let mounted = true;

    const initializeAuth = async () => {
      try {
        console.log('üîÑ Inicializando autenticaci√≥n...');

        // Verificar si hay sesi√≥n demo en localStorage
        const demoSession = localStorage.getItem('mtz-demo-session');
        if (demoSession) {
          const demoData = JSON.parse(demoSession);
          console.log('‚úÖ Sesi√≥n demo encontrada:', demoData.email);
          setUser(demoData.user);
          setUserProfile(demoData.profile);
          setRole(demoData.role);
          setPermissions(demoData.permissions || {});
          setLoading(false);
          return;
        }

        // Obtener sesi√≥n actual de Supabase
        const {
          data: { session },
          error,
        } = await supabase.auth.getSession();

        if (error) {
          console.warn('‚ö†Ô∏è Error obteniendo sesi√≥n:', error.message);
          // No establecer error para errores de token, solo limpiar sesi√≥n
          if (error.message.includes('Invalid Refresh Token')) {
            console.log('üîÑ Limpiando sesi√≥n inv√°lida...');
            await supabase.auth.signOut();
          }
        }

        if (mounted) {
          if (session?.user) {
            console.log('‚úÖ Sesi√≥n activa encontrada:', session.user.email);
            setUser(session.user);
            await loadUserProfile(session.user);
          } else {
            console.log('‚ÑπÔ∏è No hay sesi√≥n activa');
            setUser(null);
            setUserProfile(null);
            setRole(null);
            setPermissions({});
          }
          setLoading(false);
        }
      } catch (err) {
        console.error('‚ùå Error en inicializaci√≥n:', err);
        if (mounted) {
          setError(err.message);
          setUser(null);
          setUserProfile(null);
          setRole(null);
          setPermissions({});
          setLoading(false);
        }
      }
    };

    initializeAuth();

    // Escuchar cambios de autenticaci√≥n
    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange(async (event, session) => {
      if (!mounted) return;

      console.log('üîÑ Cambio de auth detectado:', event);

      switch (event) {
        case 'SIGNED_IN':
          console.log('‚úÖ Usuario autenticado:', session.user.email);
          setUser(session.user);
          await loadUserProfile(session.user);
          setError(null);
          break;
        case 'SIGNED_OUT':
          console.log('‚ÑπÔ∏è Usuario desautenticado');
          setUser(null);
          setUserProfile(null);
          setRole(null);
          setPermissions({});
          setError(null);
          break;
        case 'TOKEN_REFRESHED':
          console.log('üîÑ Token actualizado');
          break;
        default:
          console.log('‚ÑπÔ∏è Evento de auth:', event);
      }

      setLoading(false);
    });

    return () => {
      mounted = false;
      subscription.unsubscribe();
    };
  }, []);

  // =====================================================================
  // üéØ VALOR DEL CONTEXTO
  // =====================================================================

  const value = {
    user,
    userProfile,
    role,
    permissions,
    loading,
    error,
    signIn,
    signUp,
    signOut,
    hasPermission,
    isAuthenticated: !!user,
  };

  return <AuthContext.Provider value={value}>{children}</AuthContext.Provider>;
};

export default AuthContext;
